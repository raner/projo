package pro.projo.annotations;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

/**
* The {@link Proxy @Proxy} annotation is used for marking the method of a proxy interface that
* returns a reference to the wrapped objects. Default methods that override their original (super)
* implementation can call the marked method to get access to the wrapped object.
*
* @author Mirko Raner
**/
@Target(METHOD)
@Retention(RUNTIME)
public @interface Proxy
{
    // No attributes
}
